# Functions
install.cran <- function(package, version, host="cloud.r-project.org"){
	install.packages(paste0("https://",host,"/src/contrib/Archive/",package,"/",package,"_",version,".tar.gz"), repos = NULL)
}

install.bioconductor <- function(package, version, bioconductor_version, source = "bioc"){
	install.packages(paste0("https://www.bioconductor.org/packages/",bioconductor_version,"/",source,"/src/contrib/",package,"_",version,".tar.gz"), repos = NULL)
}

